"""
Calculator

Functions:
- sum
-substraction
-multiply
-divide
"""



def sum(num_1,num_2):
    result = num_1 + num_2
    return result

def substraction(num_1,num_2):
    result = num_1 - num_2 
    return result

def multiply(num_1,num_2):
    result = num_1 * num_2
    return result

def divide(num_1, num_2):
    result = num_1 / num_2
    return result
